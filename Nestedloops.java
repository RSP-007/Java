public class Nestedloops {
    public static void main(String[] args){

        //nested loop =A LOOP INSIDE ANOTHER LOOP
        //             USED OFTEN WITH MATRICES OR DSA

        for(int i=1;i<=3;i++){
            for(int j=1; j<=9;j++){

                System.out.print(j +" ");

            }
            System.out.println();

        }

    }
}

